def generate_bot_zip(config: dict, uploads_dir: Optional[str] = None) -> bytes:
    """
    Builds the full bot project as a ZIP and returns it as bytes.

    `uploads_dir` should be the same folder /api/upload_media saves into, so
    any "/media/<file>" references in the config can be embedded as local
    assets in the generated project.
    """
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, mode="w", compression=zipfile.ZIP_DEFLATED) as zf:
        resolved_config = _rewrite_config_assets(zf, config, uploads_dir)

        zf.writestr("bot.py", BOT_PY)
        zf.writestr("config.json", json.dumps(resolved_config, ensure_ascii=False, indent=2))
        zf.writestr("requirements.txt", REQUIREMENTS_TXT)
        zf.writestr(".env.example", ENV_EXAMPLE)
        zf.writestr("README.md", README_MD)

    return buf.getvalue()
